// ===== array method ======
var colors=["red","green","yellow"]
// 1) push method     (add last)
colors.push("blue")
console.log(colors);

// 2)pop method (remove last element)

var popped_object=colors.pop()
console.log(colors);

// 3)unshift   (add begining)

colors.unshift("orange")
console.log(colors);

// 4)shift   (remove begining)

colors.shift()
console.log(colors);

//  5)includes
console.log(colors.includes("red"));

