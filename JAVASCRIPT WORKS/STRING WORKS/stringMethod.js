var text="javascript work"
//        0123456789
// charAt(index) return character at specified index
console.log(text.charAt(2));

// index(char)
console.log(text.indexOf("as"));

// toUpperCase()
console.log(text.toUpperCase());

// toLowerCase()
console.log(text.toLowerCase());

// startsWith("str") return true or false text start chyenane ja vech anel true alenkil false
console.log(text.startsWith("ja"));   //rreturn true/false

// endsWith("str")
console.log(text.endsWith("pt"));    //rreturn true/false

// includes("str")
console.log(text.includes("scr"));  //rreturn true/false

// slice(start,end)
console.log(text.slice(0,4));

// split()
console.log(text.split(" "));